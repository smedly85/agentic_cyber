def _import(name: str, pip_hint: str | None = None) -> Any:
    try:
        return __import__(name)
    except ImportError as exc:  # pragma: no cover - environment dependent
        hint = pip_hint or name
        raise SystemExit(
            f"Missing Python package '{name}'.\n"
            f"Install it with: python3 -m pip install {hint}\n"
            f"(original error: {exc})"
        )


def _tree_sitter_c_language() -> Any:
    from tree_sitter import Language

    import tree_sitter_c as tsc

    try:
        return Language(tsc.language())
    except TypeError:
        # Older tree-sitter (<0.22) needs a two-arg form.
        return Language(tsc.language(), "c")


def _make_parser() -> Any:
    from tree_sitter import Parser

    language = _tree_sitter_c_language()
    try:
        return Parser(language)
    except TypeError:
        parser = Parser()
        parser.set_language(language)
        return parser


_PARSER_CACHE: Any = None


def get_parser() -> Any:
    global _PARSER_CACHE
    if _PARSER_CACHE is None:
        _import("tree_sitter", "tree-sitter")
        _import("tree_sitter_c", "tree-sitter-c")
        _PARSER_CACHE = _make_parser()
    return _PARSER_CACHE


