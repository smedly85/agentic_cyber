#!/usr/bin/env python3
"""Measure implementation diversity across N-version C programs.

Given a set of C source files that implement the same specification (e.g.
independent LLM samples of ``mkdir``), this tool answers a narrower question
than generic code similarity: *how much does the implementation actually
differ*, at several levels that are each grounded in an established
similarity/clone-detection paradigm, so that the numbers are defensible in a
research write-up rather than an ad-hoc score.

Levels (see docs/diversity_methodology.md for the full citation list and the
"what each level contributes" interpretation contract):

  1. lexical    - normalized Levenshtein (Type-1/3 clones) + Type-2 token
                  winnowing/Jaccard fingerprints (CCFinder / MOSS paradigm).
  2. ast        - normalized tree edit distance over tree-sitter C parse
                  trees, computed with APTED.
  3. api        - Jaccard distance over the set of called functions
                  ("strategy" fingerprint), plus descriptive lizard
                  complexity stats (NOT used as a distance).
  4. attack     - Jaccard/cosine distance over a small vector of
                  security-relevant syntactic constructs (unsafe string
                  calls, bounded-but-risky calls, fixed-size stack buffers,
                  heap calls, indexing operations) - the level that speaks
                  most directly to exploit non-transferability.
  5. neural     - optional (--neural): symmetrized 1-CodeBLEU and code-model
                  embedding cosine distance. Heavier deps (torch,
                  transformers, codebleu); reported as a literature
                  comparability point, not load-bearing for the other
                  levels.

Every level is reported as its own N x N distance matrix; this tool does not
compute a single weighted "diversity score" (see methodology doc for why).

Usage:
    python3 scripts/measure_diversity.py FILE_OR_GLOB [FILE_OR_GLOB ...] \\
        [--reference PATH] [--neural] [--out-dir DIR] [--calibrate]
"""

