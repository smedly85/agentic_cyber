def call_set(parsed: ParsedSource) -> set[str]:
    calls: set[str] = set()
    for node in iter_nodes(parsed.root):
        if node.type == "call_expression":
            name = tree_sitter_call_name(node, parsed.data)
            if name:
                calls.add(name)
    return calls


def call_set_similarity(a: ParsedSource, b: ParsedSource) -> float:
    return jaccard(call_set(a), call_set(b))


def lizard_metrics(path: Path) -> dict[str, float] | None:
    import shutil
    import subprocess

    executable = shutil.which("lizard")
    if executable is None:
        # Fall back to the running interpreter's own venv bin/ - `lizard`
        # is a Python console-script, so it lives next to sys.executable
        # even when the venv hasn't been `activate`d (PATH untouched).
        candidate = Path(sys.executable).with_name("lizard")
        if candidate.exists():
            executable = str(candidate)
    if executable is None:
        return None
    result = subprocess.run(
        [executable, "--csv", str(path)],
        capture_output=True,
        text=True,
        timeout=60,
    )
    if result.returncode != 0:
        return None
    import csv
    import io

    totals = {
        "function_count": 0.0,
        "total_nloc": 0.0,
        "total_cyclomatic_complexity": 0.0,
        "total_token_count": 0.0,
        "max_cyclomatic_complexity": 0.0,
        "max_function_length": 0.0,
    }
    for row in csv.reader(io.StringIO(result.stdout)):
        if len(row) < 8:
            continue
        try:
            nloc, complexity, tokens, _params, length = (
                float(row[0]),
                float(row[1]),
                float(row[2]),
                row[3],
                float(row[4]),
            )
        except ValueError:
            continue
        totals["function_count"] += 1
        totals["total_nloc"] += nloc
        totals["total_cyclomatic_complexity"] += complexity
        totals["total_token_count"] += tokens
        totals["max_cyclomatic_complexity"] = max(
            totals["max_cyclomatic_complexity"], complexity
        )
        totals["max_function_length"] = max(
            totals["max_function_length"], length
        )
    return totals


# ---------------------------------------------------------------------------
# Level 4: attack-surface / security-relevant construct profile
# ---------------------------------------------------------------------------

UNSAFE_CALLS = {"strcpy", "strcat", "sprintf", "gets", "vsprintf"}
BOUNDED_RISKY_CALLS = {
    "strncpy",
    "strncat",
    "snprintf",
    "memcpy",
    "memmove",
    "stpcpy",
}
HEAP_CALLS = {"malloc", "calloc", "realloc", "free", "strdup", "reallocarray"}

ATTACK_SURFACE_CATEGORIES = (
    "unsafe_calls",
    "bounded_risky_calls",
    "heap_calls",
    "fixed_stack_buffers",
    "indexing_ops",
)


def _is_fixed_size_array_declarator(node: Any, data: bytes) -> bool:
    """True for e.g. ``char buf[PATH_MAX];`` / ``char buf[64];`` -
    a local array declarator whose size is a literal or a (presumably
    constant) identifier, as opposed to a variable-length/pointer form.
    """
    if node.type != "array_declarator":
        return False
    size = node.child_by_field_name("size")
    return size is not None and size.type in ("number_literal", "identifier")


def attack_surface_vector(parsed: ParsedSource) -> dict[str, int]:
    counts = {c: 0 for c in ATTACK_SURFACE_CATEGORIES}
    for node in iter_nodes(parsed.root):
        if node.type == "call_expression":
            name = tree_sitter_call_name(node, parsed.data)
            if name in UNSAFE_CALLS:
                counts["unsafe_calls"] += 1
            elif name in BOUNDED_RISKY_CALLS:
                counts["bounded_risky_calls"] += 1
            elif name in HEAP_CALLS:
                counts["heap_calls"] += 1
        elif node.type == "array_declarator":
            if _is_fixed_size_array_declarator(node, parsed.data):
                counts["fixed_stack_buffers"] += 1
        elif node.type == "subscript_expression":
            counts["indexing_ops"] += 1
    return counts


def _cosine(a: Sequence[float], b: Sequence[float]) -> float:
    dot = sum(x * y for x, y in zip(a, b))
    norm_a = sum(x * x for x in a) ** 0.5
    norm_b = sum(y * y for y in b) ** 0.5
    if norm_a == 0.0 and norm_b == 0.0:
        return 1.0
    if norm_a == 0.0 or norm_b == 0.0:
        return 0.0
    return dot / (norm_a * norm_b)


def attack_surface_similarity(a: ParsedSource, b: ParsedSource) -> float:
    va = attack_surface_vector(a)
    vb = attack_surface_vector(b)
    vec_a = [va[c] for c in ATTACK_SURFACE_CATEGORIES]
    vec_b = [vb[c] for c in ATTACK_SURFACE_CATEGORIES]
    cosine_sim = _cosine(vec_a, vec_b)
    present_a = {c for c in ATTACK_SURFACE_CATEGORIES if va[c] > 0}
    present_b = {c for c in ATTACK_SURFACE_CATEGORIES if vb[c] > 0}
    jaccard_sim = jaccard(present_a, present_b)
    # Average the scale-sensitive (cosine) and presence-only (Jaccard)
    # views; both are reported individually in per-variant diagnostics.
    return (cosine_sim + jaccard_sim) / 2.0


