def cluster_variants(
    variants: Sequence[Variant],
    pairwise: Mapping[tuple[str, str], float],
    threshold: float,
) -> dict[str, Any]:
    if len(variants) < 3:
        return {"skipped": "need >=3 variants for clustering"}
    _import("numpy", "numpy")
    _import("sklearn", "scikit-learn")
    import numpy as np
    from sklearn.cluster import AgglomerativeClustering
    from sklearn.metrics import silhouette_score

    labels = [v.label for v in variants]
    dist = np.array(
        [[1.0 - pairwise[(a, b)] for b in labels] for a in labels]
    )
    if np.isnan(dist).any():
        return {"skipped": "one or more pairwise distances is NaN (see warnings above)"}
    np.fill_diagonal(dist, 0.0)
    model = AgglomerativeClustering(
        metric="precomputed",
        linkage="average",
        distance_threshold=threshold,
        n_clusters=None,
    )
    assignment = model.fit_predict(dist)
    n_clusters = len(set(assignment))
    result: dict[str, Any] = {
        "distance_threshold": threshold,
        "n_clusters": n_clusters,
        "assignment": {label: int(c) for label, c in zip(labels, assignment)},
    }
    if 2 <= n_clusters <= len(variants) - 1:
        result["silhouette"] = float(
            silhouette_score(dist, assignment, metric="precomputed")
        )
    else:
        result["silhouette"] = None
    return result


