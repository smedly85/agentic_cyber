_CALIBRATION_FIXTURE = b"""\
#include <stdio.h>
#include <string.h>

static int add_one(int value) {
    int result = value + 1;
    return result;
}

int main(int argc, char *argv[]) {
    char buffer[64];
    strncpy(buffer, argv[0], sizeof(buffer) - 1);
    buffer[sizeof(buffer) - 1] = '\\0';
    int total = add_one(41);
    printf("%s says %d\\n", buffer, total);
    return 0;
}
"""


def _rename_identifiers(source: bytes) -> bytes:
    """Produce a Type-2-clone control: every identifier token is renamed
    to a distinct, deterministic placeholder while everything else
    (structure, literals, keywords, operators) is left untouched."""
    parsed = parse_source(source)
    renames: dict[str, str] = {}
    counter = itertools.count(1)

    edits: list[tuple[int, int, bytes]] = []
    for node in iter_nodes(parsed.root):
        if node.type == "identifier" and not node.children:
            original = parsed.data[node.start_byte : node.end_byte].decode("utf-8")
            if original not in renames:
                renames[original] = f"v{next(counter)}"
            edits.append((node.start_byte, node.end_byte, renames[original].encode()))

    edits.sort(key=lambda e: e[0])
    out = bytearray()
    cursor = 0
    for start, end, replacement in edits:
        out += source[cursor:start]
        out += replacement
        cursor = end
    out += source[cursor:]
    return bytes(out)


def run_calibration() -> bool:
    """Run the identical-copy / renamed-copy sanity controls from the
    methodology's validity-controls section. Prints a pass/fail table and
    returns True iff every expectation held.
    """
    base = _CALIBRATION_FIXTURE
    renamed = _rename_identifiers(base)

    identical = Variant("base", Path("<calibration:base>"), base)
    identical_copy = Variant("base_copy", Path("<calibration:base_copy>"), base)
    renamed_copy = Variant("renamed", Path("<calibration:renamed>"), renamed)

    metrics = build_classical_metrics()
    ok = True
    print("Calibration: identical-copy control (expect ~1.0 at every level)")
    for m in metrics:
        sim = m.fn(identical, identical_copy)
        passed = sim > 0.999
        ok &= passed
        print(f"  [{'OK' if passed else 'FAIL'}] {m.key:22s} = {sim:.4f}")

    print(
        "\nCalibration: renamed-copy control "
        "(expect ast_ted & lexical_winnowing ~1.0, lexical_levenshtein < 1.0)"
    )
    expectations = {
        "lexical_levenshtein": lambda s: s < 0.999,
        "lexical_winnowing": lambda s: s > 0.999,
        "ast_ted": lambda s: s > 0.999,
        "api_callset": lambda s: True,  # renaming also renames call names; informational only
        "attack_surface": lambda s: True,
    }
    for m in metrics:
        sim = m.fn(identical, renamed_copy)
        check = expectations.get(m.key, lambda s: True)
        passed = check(sim)
        ok &= passed
        print(f"  [{'OK' if passed else 'FAIL'}] {m.key:22s} = {sim:.4f}")

    print(f"\nCalibration {'PASSED' if ok else 'FAILED'}.")
    return ok


