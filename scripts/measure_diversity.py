def strip_comments(parsed: ParsedSource) -> str:
    """Remove tree-sitter 'comment' node spans, collapse whitespace."""
    data = parsed.data
    comment_spans = [
        (n.start_byte, n.end_byte)
        for n in iter_nodes(parsed.root)
        if n.type == "comment"
    ]
    comment_spans.sort()
    out = bytearray()
    cursor = 0
    for start, end in comment_spans:
        out += data[cursor:start]
        cursor = end
    out += data[cursor:]
    text = out.decode("utf-8", errors="replace")
    return re.sub(r"\s+", " ", text).strip()


def normalized_levenshtein_similarity(a: ParsedSource, b: ParsedSource) -> float:
    rapidfuzz = _import("rapidfuzz", "rapidfuzz")
    from rapidfuzz.fuzz import ratio as levenshtein_ratio

    return levenshtein_ratio(strip_comments(a), strip_comments(b)) / 100.0


_LEAF_LITERAL_TYPES = {
    "number_literal": "NUM",
    "string_literal": "STR",
    "char_literal": "CHAR",
}


def type2_token_stream(parsed: ParsedSource) -> list[str]:
    """CCFinder-style Type-2 normalized token stream.

    Identifiers and literals are rewritten to a class placeholder;
    keywords, operators and punctuation are kept verbatim. This is what
    makes the resulting similarity invariant to identifier renaming and
    literal-value changes while still being sensitive to structure and
    vocabulary (which keywords/operators are used).
    """
    tokens: list[str] = []
    data = parsed.data
    for node in iter_nodes(parsed.root):
        if node.children or node.type == "comment":
            continue
        if node.type == "identifier":
            tokens.append("ID")
        elif node.type in _LEAF_LITERAL_TYPES:
            tokens.append(_LEAF_LITERAL_TYPES[node.type])
        else:
            tokens.append(node.type)
    return tokens


def winnowing_fingerprints(tokens: Sequence[str], k: int = 5, w: int = 4) -> set[int]:
    """Schleimer/Wilkerson/Aiken (SIGMOD 2003) winnowing fingerprint set.

    k = k-gram length in tokens, w = window size in hashes. Standard
    winnowing: within each window of k-gram hashes, select the minimum
    (rightmost on ties), de-duplicating consecutively repeated selections
    at the same position.
    """
    if len(tokens) < k:
        k = max(1, len(tokens))
    grams = [
        tuple(tokens[i : i + k]) for i in range(0, max(1, len(tokens) - k + 1))
    ]
    hashes = [
        int.from_bytes(
            hashlib.sha1("\x1f".join(g).encode("utf-8")).digest()[:8], "big"
        )
        for g in grams
    ]
    if not hashes:
        return set()

    fingerprints: set[int] = set()
    last_selected_index = -1
    for start in range(0, max(1, len(hashes) - w + 1)):
        window = hashes[start : start + w] or hashes[start:]
        min_value = min(window)
        # Rightmost occurrence of the minimum in this window.
        min_index_in_window = max(
            i for i, v in enumerate(window) if v == min_value
        )
        min_index = start + min_index_in_window
        if min_index != last_selected_index:
            fingerprints.add(hashes[min_index])
            last_selected_index = min_index
    return fingerprints


def jaccard(a: set[Any], b: set[Any]) -> float:
    if not a and not b:
        return 1.0
    union = a | b
    if not union:
        return 1.0
    return len(a & b) / len(union)


def token_winnowing_similarity(a: ParsedSource, b: ParsedSource) -> float:
    fp_a = winnowing_fingerprints(type2_token_stream(a))
    fp_b = winnowing_fingerprints(type2_token_stream(b))
    return jaccard(fp_a, fp_b)


