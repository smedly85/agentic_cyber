_EMBEDDING_MODEL_CACHE: dict[str, tuple[Any, Any]] = {}


def codebleu_similarity(a_text: str, b_text: str) -> float:
    codebleu_mod = _import("codebleu", "codebleu")
    calc_codebleu = codebleu_mod.calc_codebleu
    # CodeBLEU is reference-based (asymmetric n-gram precision direction);
    # symmetrize by averaging both directions.
    forward = calc_codebleu([a_text], [b_text], lang="c")["codebleu"]
    backward = calc_codebleu([b_text], [a_text], lang="c")["codebleu"]
    return (forward + backward) / 2.0


def _load_embedding_model(model_name: str) -> tuple[Any, Any]:
    if model_name in _EMBEDDING_MODEL_CACHE:
        return _EMBEDDING_MODEL_CACHE[model_name]
    torch = _import("torch", "torch")
    transformers = _import("transformers", "transformers")
    tokenizer = transformers.AutoTokenizer.from_pretrained(model_name)
    model = transformers.AutoModel.from_pretrained(model_name)
    model.eval()
    _EMBEDDING_MODEL_CACHE[model_name] = (tokenizer, model)
    return tokenizer, model


def embed(text: str, model_name: str) -> Any:
    torch = _import("torch", "torch")
    tokenizer, model = _load_embedding_model(model_name)
    inputs = tokenizer(
        text, return_tensors="pt", truncation=True, max_length=512
    )
    with torch.no_grad():
        outputs = model(**inputs)
    hidden = outputs.last_hidden_state[0]
    mask = inputs["attention_mask"][0].unsqueeze(-1).float()
    pooled = (hidden * mask).sum(0) / mask.sum().clamp(min=1.0)
    return pooled


def embedding_similarity(a_text: str, b_text: str, model_name: str) -> float:
    torch = _import("torch", "torch")
    ea = embed(a_text, model_name)
    eb = embed(b_text, model_name)
    sim = torch.nn.functional.cosine_similarity(ea.unsqueeze(0), eb.unsqueeze(0))
    return float(sim.item())


