@dataclass
class Variant:
    label: str
    path: Path
    source: bytes

    @property
    def text(self) -> str:
        return self.source.decode("utf-8", errors="replace")


def derive_label(path: Path, all_paths: Sequence[Path]) -> str:
    """Pick a short, human-legible, unique label for a variant path.

    Prefers a distinguishing ancestor directory (e.g. a ``temp-0p2`` run
    directory) over the bare filename, since files generated by the
    pipeline are almost always named identically (``new_mkdir.c``) across
    variants and it's the run directory that distinguishes them. But if
    the filename *stems* are already distinctive across the whole input
    set (e.g. vendored anchors like ``gnu_coreutils_mkdir.c`` vs
    ``busybox_mkdir.c``, all sitting in one directory), prefer the stem -
    it is more informative than a repeated parent directory name.
    """
    parts = path.parts
    temp_dirs = [p for p in parts if re.match(r"^temp-[0-9p]+$", p)]
    if temp_dirs:
        return temp_dirs[-1]

    stems = [p.stem for p in all_paths]
    if len(set(stems)) == len(stems):
        return path.stem

    # Fall back to the parent directory name, then the stem.
    if path.parent.name and path.parent.name not in (".", ""):
        return path.parent.name
    return path.stem


def load_variants(patterns: Sequence[str]) -> list[Variant]:
    paths: list[Path] = []
    for pattern in patterns:
        matches = sorted(Path(p) for p in glob.glob(pattern, recursive=True))
        if not matches and Path(pattern).exists():
            matches = [Path(pattern)]
        if not matches:
            raise SystemExit(f"No files matched: {pattern}")
        paths.extend(matches)

    # De-duplicate while preserving order.
    seen: set[Path] = set()
    unique_paths: list[Path] = []
    for p in paths:
        resolved = p.resolve()
        if resolved not in seen:
            seen.add(resolved)
            unique_paths.append(p)

    labels_seen: dict[str, int] = {}
    variants: list[Variant] = []
    for p in unique_paths:
        label = derive_label(p, unique_paths)
        if label in labels_seen:
            labels_seen[label] += 1
            label = f"{label}#{labels_seen[label]}"
        else:
            labels_seen[label] = 0
        variants.append(Variant(label=label, path=p, source=p.read_bytes()))
    return variants


