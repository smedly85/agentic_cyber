def _metric_codebleu(a: Variant, b: Variant) -> float:
    return codebleu_similarity(a.text, b.text)


def _metric_embedding(a: Variant, b: Variant, model_name: str) -> float:
    return embedding_similarity(a.text, b.text, model_name)


