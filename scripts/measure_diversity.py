def tree_sitter_function_name(node: Any, data: bytes) -> str | None:
    """Extract a function definition's identifier name.

    Ported from scripts/analyze_experiment.py (pure function, no
    dependency on that module's larger pipeline).
    """
    declarator = node.child_by_field_name("declarator")
    if declarator is None:
        return None
    stack = [declarator]
    while stack:
        current = stack.pop()
        if current.type == "identifier":
            return data[current.start_byte : current.end_byte].decode(
                "utf-8", errors="replace"
            )
        stack.extend(reversed(current.children))
    return None


def tree_sitter_call_name(node: Any, data: bytes) -> str | None:
    """Extract a call_expression's callee identifier name.

    Ported from scripts/analyze_experiment.py.
    """
    function = node.child_by_field_name("function")
    if function is None:
        return None
    if function.type == "identifier":
        return data[function.start_byte : function.end_byte].decode(
            "utf-8", errors="replace"
        )
    return None


@dataclass
class ParsedSource:
    root: Any
    data: bytes


_PARSE_CACHE: dict[bytes, ParsedSource] = {}


def parse_source(source: bytes) -> ParsedSource:
    key = hashlib.sha256(source).digest()
    cached = _PARSE_CACHE.get(key)
    if cached is not None:
        return cached
    parser = get_parser()
    tree = parser.parse(source)
    parsed = ParsedSource(root=tree.root_node, data=source)
    _PARSE_CACHE[key] = parsed
    return parsed


def iter_nodes(node: Any) -> Iterable[Any]:
    stack = [node]
    while stack:
        current = stack.pop()
        yield current
        stack.extend(reversed(current.children))


