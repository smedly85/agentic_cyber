# Level 2: AST / structural similarity (APTED tree edit distance)
# ---------------------------------------------------------------------------


class ASTTooLargeError(RuntimeError):
    """Raised when a parse tree exceeds MAX_AST_NODES; TED's cost blows up
    much faster than linearly in tree size for large/dissimilar trees, so
    this is a deliberate safety valve rather than a bug."""


MAX_AST_NODES = 20_000


def _to_apted_tree(node: Any) -> Any:
    # Only *named* nodes go into the tree (tree-sitter's term for
    # syntactically meaningful nodes - identifiers, expressions,
    # statements, ...), excluding anonymous punctuation/keyword leaves
    # (`(`, `;`, `{`, ...) and comments. This is what "AST" conventionally
    # means (as opposed to a full concrete/parse tree) and, as a large
    # side benefit, keeps trees small enough for APTED to be tractable:
    # an unfiltered concrete-syntax tree can be 1.5-2x the named-node
    # count but take orders of magnitude longer to align.
    from apted.helpers import Tree

    children = [
        _to_apted_tree(c)
        for c in node.children
        if c.is_named and c.type != "comment"
    ]
    return Tree(node.type, *children)


def _tree_size(node: Any) -> int:
    total = 0
    for n in iter_nodes(node):
        if not n.is_named or n.type == "comment":
            continue
        total += 1
    return total


def ast_tree_edit_similarity(a: ParsedSource, b: ParsedSource) -> float:
    _import("apted", "apted")
    from apted import APTED, Config

    size_a, size_b = _tree_size(a.root), _tree_size(b.root)
    if size_a > MAX_AST_NODES or size_b > MAX_AST_NODES:
        raise ASTTooLargeError(
            f"AST too large for exact tree edit distance "
            f"({size_a} / {size_b} named nodes, limit {MAX_AST_NODES}); "
            "skipped (see docs/diversity_methodology.md scalability note)."
        )
    tree_a = _to_apted_tree(a.root)
    tree_b = _to_apted_tree(b.root)
    ted = APTED(tree_a, tree_b, Config()).compute_edit_distance()
    max_size = max(size_a, size_b, 1)
    return max(0.0, 1.0 - ted / max_size)


