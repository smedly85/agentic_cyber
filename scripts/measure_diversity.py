def cross_level_correlation(
    level_vectors: Mapping[str, Sequence[float]]
) -> tuple[list[str], list[list[float]]]:
    _import("scipy", "scipy")
    from scipy.stats import spearmanr

    names = list(level_vectors)
    matrix = [[0.0] * len(names) for _ in names]
    for i, ni in enumerate(names):
        for j, nj in enumerate(names):
            if i == j:
                matrix[i][j] = 1.0
                continue
            vi, vj = level_vectors[ni], level_vectors[nj]
            if len(vi) < 2 or len(set(vi)) < 2 or len(set(vj)) < 2:
                matrix[i][j] = float("nan")
                continue
            corr, _p = spearmanr(vi, vj)
            matrix[i][j] = float(corr)
    return names, matrix


