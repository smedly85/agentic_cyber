#!/usr/bin/env python3
"""
Hand-authored cases: the constraint-table error catalog (exact frozen
stderr), semantic quirks pinned as golden filesystem trees, adversarial
path/name stress cases, and filesystem fault injection. This is where the
"hardcoding" lives for the mkdir suite -- everything else (singles, pairs,
random) is generated by combos.py from the flag/corpus models.

All expected outputs are captured from GNU mkdir at freeze time, not
hand-computed; what's hand-authored here is which SCENARIOS to probe.
"""
from __future__ import annotations

import os
import sys

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from gen.combos import make_case  # noqa: E402


def _dir(path: str, mode: str = "0755") -> dict:
    return {"path": path, "type": "dir", "mode": mode}


def _file(path: str, mode: str = "0644") -> dict:
    return {"path": path, "type": "file", "mode": mode, "contents_b64": ""}


def _symlink(path: str, target: str) -> dict:
    return {"path": path, "type": "symlink", "target": target}


# --- curated: error catalog + semantic quirks -------------------------------

def build(corpus) -> list[dict]:
    cases = []

    # --- error catalog: exact frozen stderr, cross-checked against the
    # constraint model's predicted exit code at freeze time.
    cases.append(make_case(
        "err-eexist", ["existing"], [], [_dir("existing")],
        tags=["curated", "negative"], rule_id="EEXIST", exact_stderr=True))

    cases.append(make_case(
        "err-enoent", ["a/b"], [], [],
        tags=["curated", "negative"], rule_id="ENOENT", exact_stderr=True))

    cases.append(make_case(
        "err-notdir", ["afile/b"], [], [_file("afile")],
        tags=["curated", "negative"], rule_id="ENOTDIR", exact_stderr=True))

    cases.append(make_case(
        "err-bad-mode", ["-m", "u+z", "d"], ["-m"], [],
        tags=["curated", "negative"], rule_id="M_bad_mode", exact_stderr=True))

    cases.append(make_case(
        "err-no-operand", [], [], [],
        tags=["curated", "negative"], rule_id="no_operand", exact_stderr=True))

    cases.append(make_case(
        "err-unknown", ["--no-such-flag", "d"], [], [],
        tags=["curated", "negative"], rule_id="unknown_flag", exact_stderr=True))

    cases.append(make_case(
        "err-dashdir", ["--", "-weird"], [], [],
        tags=["curated"], check="golden"))

    # --- semantic quirks: pinned as golden tree+mode, proving the suite can
    # catch a candidate that gets these subtleties wrong.

    # GNU mkdir applies -m ONLY to the final requested directory; parents
    # auto-created by -p get the default (0777 & ~umask) mode regardless.
    cases.append(make_case(
        "quirk-p-mode-last-only", ["-p", "-m", "0700", "a/b/c"], ["-p", "-m"],
        [], tags=["curated"], check="golden", umask="0022"))

    cases.append(make_case(
        "quirk-umask-default", ["plain"], [], [],
        tags=["curated"], check="golden", umask="0022"))

    cases.append(make_case(
        "quirk-m-ignores-umask", ["-m", "0777", "plain"], ["-m"], [],
        tags=["curated"], check="golden", umask="0077"))

    cases.append(make_case(
        "quirk-verbose-output", ["-v", "vdir"], ["-v"], [],
        tags=["curated"], check="golden"))

    cases.append(make_case(
        "quirk-verbose-parents", ["-p", "-v", "nested/vdir2"], ["-p", "-v"],
        [], tags=["curated"], check="golden"))

    cases.append(make_case(
        "quirk-trailing-slash", ["trailed/"], [], [],
        tags=["curated"], check="golden"))

    # -p is idempotent: an already-existing target succeeds (exit 0)
    # instead of erroring, unlike the bare EEXIST case above.
    cases.append(make_case(
        "quirk-p-eexist-ok", ["-p", "existing"], ["-p"], [_dir("existing")],
        tags=["curated"], check="golden"))

    # multi-operand partial failure: one bad operand must not prevent the
    # good ones from being created; exit code still reflects the failure.
    cases.append(make_case(
        "quirk-multi-partial-fail", ["existing", "newdir"], [],
        [_dir("existing")], tags=["curated", "negative"],
        rule_id="EEXIST", exact_stderr=False))

    cases.append(make_case(
        "quirk-special-bits", ["-m", "4755", "suid_d"], ["-m"], [],
        tags=["curated"], check="golden"))

    cases.append(make_case(
        "quirk-sticky-symbolic", ["-m", "+t", "sticky_d"], ["-m"], [],
        tags=["curated"], check="golden"))

    cases.append(make_case(
        "quirk-symlink-parent-p", ["-p", "link/child"], ["-p"],
        [_dir("real"), _symlink("link", "real")],
        tags=["curated"], check="golden"))

    # GNU mkdir never chmods a pre-existing directory, even under -p -m:
    # the whole tree would still golden-check fine here, but this is the
    # canonical use of check="property:idempotent_p" (see props.py) --
    # what matters is that THIS path's mode is provably untouched.
    existing_case = make_case(
        "quirk-p-m-existing-mode-unchanged", ["-p", "-m", "0700", "existing_dir"],
        ["-p", "-m"], [_dir("existing_dir", "0755")],
        tags=["curated"], check="property:idempotent_p")
    existing_case["preserve_mode"] = {"existing_dir": "0755"}
    cases.append(existing_case)

    return cases


# --- adversarial: stress inputs x a handful of flag sets --------------------

_ADV_FLAG_SETS = [
    ([], []),
    (["-p"], ["-p"]),
    (["-p", "-v"], ["-p", "-v"]),
    (["-m", "0700", "-p"], ["-m", "-p"]),
]


def build_adversarial(corpus) -> list[dict]:
    from corpus import corpus as corpus_mod
    adv = corpus_mod.build_adversarial()
    cases = []
    for tname, target in adv.items():
        for flag_argv, flag_ids in _ADV_FLAG_SETS:
            args = list(target["args"])
            if target.get("needs_dashdash"):
                args = ["--"] + args
            argv = flag_argv + args
            fid_sig = "".join(f.lstrip("-")[:1] for f in flag_ids) or "bare"
            name = f"adv-{tname}-{fid_sig}"
            # many_operands is about volume-handling robustness (hundreds
            # of targets), not exact-mode correctness already covered by
            # singles/curated -- an exact 200-entry golden tree is brittle
            # for little benefit, so check existence only (see props.py).
            if tname == "many_operands":
                case = make_case(
                    name, argv, flag_ids, target.get("fixture"),
                    tags=["adversarial"], check="property:created")
                case["targets"] = list(target["args"])
                cases.append(case)
            else:
                cases.append(make_case(
                    name, argv, flag_ids, target.get("fixture"),
                    tags=["adversarial"], check="golden",
                    abs_targets=target.get("abs_targets")))
    return cases


# --- faults: filesystem-level fault injection -------------------------------

def build_faults(corpus) -> list[dict]:
    """Fault injection covers what a plain fixture can't express: chmod-
    based EACCES (must be applied to a live path right before the run, then
    restored for cleanup) and fd-exhaustion via rlimit. ENOTDIR/ENOENT/
    EEXIST are already exercised via plain fixtures in build() above and
    don't need the `faults` mechanism."""
    cases = []

    cases.append(make_case(
        "fault-readonly-parent", ["ro/child"], [], [_dir("ro")],
        tags=["fault"], check="none",
        faults={"readonly_parent": "ro"}, rule_id="EACCES"))

    cases.append(make_case(
        "fault-readonly-parent-with-p", ["-p", "ro/child/grandchild"], ["-p"],
        [_dir("ro")], tags=["fault"], check="none",
        faults={"readonly_parent": "ro"}, rule_id="EACCES"))

    cases.append(make_case(
        "fault-unwritable-cwd", ["newdir"], [], [],
        tags=["fault"], check="none",
        faults={"unwritable_cwd": True}, rule_id="EACCES"))

    cases.append(make_case(
        "fault-fdlimit-deep-p", ["-p", "/".join(f"d{i}" for i in range(40))],
        ["-p"], [], tags=["fault"], check="none",
        faults={"rlimit_nofile": 16}))

    return cases
